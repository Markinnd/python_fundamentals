
print(int(1.97))  # Converte float para int, resultado: 1
print(int("10"))  # Converte string para int, resultado: 10

print(float(1))  # Converte int para float, resultado: 1.0
print(float("1.97"))  # Converte string para float, resultado: 1.97

print(str(1))  # Converte int para string, resultado: '1'
print(str(1.97))  # Converte float para string, resultado: '1.97'   

valor = 10
valor_str = str(valor)  # Converte int para string
print(type(valor_str))  # Resultado: <class 'str'>, confirma que valor_str é uma string
print(type(valor))  # Resultado: <class 'int'>, confirma que valor é um int

# usando as divisões
print(10 / 3)  # Resultado: 3.3333333333333335, divisão normal (float)
print(10 // 3)  # Resultado: 3, divisão inteira (int)

