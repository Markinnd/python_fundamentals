
# Adição
print(10 + 5)  # Saída: 15
# Subtração
print(10 - 5)  # Saída: 5
# Multiplicação
print(10 * 5)  # Saída: 50
# Divisão
print(10 / 5)  # Saída: 2.0
# Divisão inteira
print(10 // 3)  # Saída: 3
# Módulo (resto da divisão)
print(10 % 3)  # Saída: 1   
# Exponenciação
print(10 ** 2)  # Saída: 100

# Precedência de operadores
print(10 + 5 * 2)  # Saída: 20 (multiplicação tem precedência sobre adição)
print((10 + 5) * 2)  # Saída: 30 (parênteses alteram a precedência) 
print(10 ** 2 * 2)  # Saída: 200 (exponenciação é avaliada antes da multiplicação)
print(10/2 * 4)  # Saída: 20.0 (divisão é avaliada antes da multiplicação)

