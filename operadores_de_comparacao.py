
saldo = 450
saque = 200

print(saldo > saque)  # Saída: True (450 é maior que 200)
print(saldo < saque)  # Saída: False (450 não é menor que 200)
print(saldo >= saque)  # Saída: True (450 é maior ou igual a 200)
print(saldo <= saque)  # Saída: False (450 não é menor ou igual a 200)
print(saldo == saque)  # Saída: False (450 não é igual a 200)
print(saldo != saque)  # Saída: True (450 é diferente de 200)   

# Comparação de strings
print("banana" > "abacaxi")  # Saída: True (banana vem depois de abacaxi na ordem alfabética)
print("banana" < "abacaxi")  # Saída: False (banana não vem antes de abacaxi na ordem alfabética)
print("banana" >= "abacaxi")    # Saída: True (banana é maior ou igual a abacaxi na ordem alfabética)
print("banana" <= "abacaxi")    # Saída: False (banana não é menor ou igual a abacaxi na ordem alfabética)
print("banana" == "abacaxi")  # Saída: False (banana não é igual a abacaxi)
print("banana" != "abacaxi")  # Saída: True (banana é diferente de abacaxi) 