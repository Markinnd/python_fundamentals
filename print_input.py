

nome = input("Informe o seu nome: ")
idade = input("Informe a sua idade: ")

print("Você informou o nome: " + nome)  # Exibe o nome informado pelo usuário
print("Você informou a idade: " + idade)  # Exibe a idade informada pelo usuário    

print("Olá, " + nome + "! Seja bem-vindo(a)!")  # Exibe uma mensagem de boas-vindas com o nome informado pelo usuário
# Exemplo de uso da função input para receber um número e exibir o tipo

print(nome, idade)  # Exibe o nome e a idade informados pelo usuário

print(nome, idade, end=" ")  # Exibe o nome e a idade informados pelo usuário, sem pular linha
print(nome, idade, sep=", ")  # Exibe o nome e a idade informados pelo usuário, separados por vírgula   
print(nome, idade, end="...\n")  # Exibe o nome e a idade informados pelo usuário, sem pular linha e com reticências no final



